mensaje_mostrado = lambda mensaje: f"Tu mensaje es: [{mensaje}]"
print(mensaje_mostrado("AAAAAAAAAA"), "\n")

###############################

valor_suma = lambda num1, num2: num1 + num2
print("El valor de la suma es: ", valor_suma(2, 3), "\n")

###############################

valor_resta = lambda num1, num2: num1 - num2
print("El valor de la resta es: ", valor_resta(3, 4), "\n")

###############################

duplicado = lambda num: num * 2
print("El numero duplicado es: ", duplicado(4), "\n")
