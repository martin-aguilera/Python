print("\nIngresa un numero menor o igual a 10:")
valor = int(input())

if valor < 10:
    print("\nok")
    print(valor, " es menor que 10\n")

elif valor == 10:
    print("\nok")
    print("tu numero es", valor, "\n")

else:
    print("\nNOOOO")
    print(valor, " es mayor que 10\n")
